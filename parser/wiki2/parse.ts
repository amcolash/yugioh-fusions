const stats = JSON.parse(readFileSync(join(__dirname, '../wiki/data.json'), 'utf-8'));

mkdirSync(join(__dirname, 'cache'), { recursive: true });

async function downloadData() {
  let errors = 0;

  for (let i = 0; i < urls.length; i++) {
    if (errors > 0) continue;
    const htmlPath = join(__dirname, `cache/${i + 1}.html`);

    try {
      let data;
      if (!existsSync(htmlPath)) {
        console.log(`Downloading data ${i + 1}/${urls.length}`);

        const response = await fetch(urls[i]);
        const buffer = await response.arrayBuffer();

        writeFileSync(htmlPath, Buffer.from(buffer));
        data = buffer.toString();

        await new Promise((resolve) => setTimeout(resolve, 200 + Math.random() * 100));
      } else {
        data = readFileSync(htmlPath, 'utf-8');
      }

      const dom = new JSDOM(data);
      const document = dom.window.document;
      const description = document.querySelector('.lore');

      stats.stats[i + 1].description = description ? description.textContent.trim() : '';

      dom.window.close();

      console.log(`Processed data ${i + 1}/${urls.length}`);
    } catch (error) {
      console.error(`Error downloading data ${i + 1}:`, error);
      rmSync(htmlPath);
      errors++;
    }
  }

  if (errors === 0) {
    writeFileSync(join(__dirname, 'data.json'), JSON.stringify(stats, null, 2));
    console.log('All data downloaded and saved to data.json');
  }
}

downloadData();
